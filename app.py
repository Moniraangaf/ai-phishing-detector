df = pd.DataFrame({
    "text": texts,
    "label": labels
})

# –í–µ–∫—Ç–æ—Ä–∏–∑–∞—Ü–∏—è
vectorizer = TfidfVectorizer()
X = vectorizer.fit_transform(df["text"])
y = df["label"]

# –û–±—É—á–µ–Ω–∏–µ –º–æ–¥–µ–ª–∏
model = LogisticRegression()
model.fit(X, y)

# –ò–Ω—Ç–µ—Ä—Ñ–µ–π—Å Streamlit
st.title("üõ°Ô∏è AI-—Å–∏—Å—Ç–µ–º–∞ –≤—ã—è–≤–ª–µ–Ω–∏—è —Ñ–∏—à–∏–Ω–≥–∞")
st.write("–í–≤–µ–¥–∏—Ç–µ —Ç–µ–∫—Å—Ç —Å–æ–æ–±—â–µ–Ω–∏—è –¥–ª—è –∞–Ω–∞–ª–∏–∑–∞")

user_input = st.text_area("–¢–µ–∫—Å—Ç —Å–æ–æ–±—â–µ–Ω–∏—è")

if st.button("–ü—Ä–æ–≤–µ—Ä–∏—Ç—å"):
    if user_input:
        vec = vectorizer.transform([user_input])
        prob = model.predict_proba(vec)[0][1]
        pred = model.predict(vec)[0]

        st.write(f"–í–µ—Ä–æ—è—Ç–Ω–æ—Å—Ç—å –º–æ—à–µ–Ω–Ω–∏—á–µ—Å—Ç–≤–∞: {round(prob*100,1)}%")

        if pred == 1:
            st.error("‚ö†Ô∏è –ü–æ–¥–æ–∑—Ä–∏—Ç–µ–ª—å–Ω–æ–µ —Å–æ–æ–±—â–µ–Ω–∏–µ")
        else:
            st.success("‚úÖ –°–æ–æ–±—â–µ–Ω–∏–µ –±–µ–∑–æ–ø–∞—Å–Ω–æ–µ")
    else:
        st.warning("–í–≤–µ–¥–∏—Ç–µ —Ç–µ–∫—Å—Ç")
